	<h2>FAQs</h2>
<ul>
	<li><strong>Q:</strong> How do I add a new product?</li>
	<li><strong>A:</strong> Click on the "Add Product" button in the top right corner of the page and fill out the product details in the form that appears.</li>
	<li><strong>Q:</strong> How do I edit an existing product?</li>
	<li><strong>A:</strong> Click on the "Edit" button next to the product you want to edit, and update the product details in the form that appears.</li>
	<li><strong>Q:</strong> How do I delete a product?</li>
	<li><strong>A:</strong> Click on the "Delete" button next to the product you want to delete, and confirm the deletion in the popup that appears.</li>
	<li><strong>Q:</strong> How do I view product details?</li>
	<li><strong>A:</strong> Click on the product name to view its details, including its price, description, and image.</li>
